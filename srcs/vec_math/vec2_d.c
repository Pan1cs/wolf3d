#include "../wolf3d.h"

float	vec2_eucl_dist(t_xy p1, t_xy p2)
{
	return (sqrtf((p2.x - p1.x) * (p2.x - p1.x)
		+ (p2.y - p1.y) * (p2.y - p1.y)));
}
